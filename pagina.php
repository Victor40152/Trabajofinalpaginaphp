            <h3>El Maximo Idolo</h3>
            <p> Jose Manuel Moreno, pasó por Independiente Medellín, donde su último partido lo jugó en 1961, a los 44 años, para el equipo colombiano -que también dirigía-, en un amistoso ante Boca Juniors: ganaron 5 a 2 con dos goles suyos. Cinco minutos antes de finalizar el partido, sin anticipar ni avisar nada, levantó los brazos, saludó al público y se fue. Era el fin de la carrera de un grande de todos los tiempos.</p>
