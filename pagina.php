            <h2>Historia</h2>
            <p>El 14 de noviembre de 1913 Alberto Uribe Piedrahíta y sus hermanos Luis y Rafael, acompañados por un grupo de paisas provenientes de Familias pudientes, tales como Guillermo Greiffenstein y José Luis Jaramillo, se entusiasmaron con la idea de crear un equipo de fútbol organizado que se enfrentara a Sporting, un equipo paisa fundado en 1913 por un grupo de antioqueños y europeos, en especial de belgas y suizos, se forjaron iniciativas, se establecieron contactos para el funcionamiento del equipo y se conforma un equipo llamado Medellín FootBall Club.</p>
        
            <h2>Nuestro Escudo</h2>
            <p>El primer escudo que caracterizó al Deportivo Independiente Medellín data de 1945, similar al de San Lorenzo de Argentina, con una franja diagonal en la mitad que decía «Medellín» y en su parte inferior en un círculo: "F.B.C", que fue el primer nombre que tuvo el club.</p>

