             ciudad de Medellín: Atlético Nacional e Independiente Medellín. Es uno de los clásicos tradicionales del fútbol colombiano, El primer clásico que se disputó en la historia entre ambos equipos fue el 12 de septiembre de 1948 y la victoria se la llevó el Independiente Medellín superando al que en el momento se denominaba Atlético Municipal
